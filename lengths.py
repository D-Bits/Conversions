
def feet_to_meters():

    feet = int(input('Enter an amount of feet to be converted into meters: '))
    meters = feet / 3.2808

    print(meters)


def meters_to_feet():

    meters = int(input('Enter an amount of meters to be converted into feet: '))
    feet = meters * 3.2808

    print(feet)


def miles_to_kilos():

    miles = int(input('Enter an amount of miles to be converted to kilometers: '))
    kilometers = miles / 0.62137

    print(kilometers)